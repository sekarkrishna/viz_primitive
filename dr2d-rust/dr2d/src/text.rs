//! Text rendering via embedded glyph atlas.
//!
//! Provides a [`GlyphAtlas`] with simple geometric glyph outlines for ASCII
//! characters (A-Z, a-z, 0-9, common punctuation). Glyphs are tessellated
//! into triangle vertices using lyon at render time.
//!
//! This module is gated behind the `text` feature flag.

use std::collections::HashMap;

use lyon::math::point;
use lyon::path::Path;
use lyon::tessellation::{
    BuffersBuilder, FillOptions, FillTessellator, FillVertex, VertexBuffers,
};

use crate::renderer::vertex::Vertex;

/// Error type for glyph operations.
#[derive(Debug, thiserror::Error)]
pub enum GlyphError {
    /// Tessellation failed for a specific character.
    #[error("Tessellation failed for character '{0}': {1}")]
    TessellationFailed(char, String),
}

/// Default glyph advance width in the unit coordinate system (0..1).
const GLYPH_ADVANCE: f32 = 0.7;

/// Embedded glyph atlas with simple geometric outlines for ASCII characters.
///
/// Each glyph is stored as a set of closed polygons (line-segment outlines)
/// that can be tessellated into triangles via lyon.
pub struct GlyphAtlas {
    /// Map from character to list of closed polygons.
    /// Each polygon is a `Vec<[f32; 2]>` of vertices in unit space (roughly 0..1).
    glyphs: HashMap<char, Vec<Vec<[f32; 2]>>>,
}

impl Default for GlyphAtlas {
    fn default() -> Self {
        Self::new()
    }
}

